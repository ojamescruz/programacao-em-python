n = float(input('>>'))
n2 = float(input('>>'))

# Comparativos

# % (Módulo) - RESTO DA CONTA DE DIVISÃO
modulo = n % n2
print(modulo)


# ** (Exponenciação)
ex = n ** n2
print (ex)

# // (Quociente)
quo = n //n2
print(quo)


# == (Igual)
print (n == n2)


# != (Diferente)
print (n != n2)


# > (Maior que)
print (n>=n2)


# < (Menor que)
print (n<=n2)
